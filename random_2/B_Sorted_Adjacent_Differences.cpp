#define FAST ios_base :: sync_with_stdio (false) ; cin.tie(0) ; cout.tie(0)
typedef pair<ll,ll>pii;
void solve(){
    ll n;cin>>n;
    vector<ll>a(n);
    for(ll i=0;i<n;i++)cin>>a[i];
    sort(all(a));
    vector<ll>v;
    ll i=0,j=n-1;
    while(i<=j){
        v.push_back(a[i]);
        if(i<j)v.push_back(a[j]);
        i++,j--;
    }
    reverse(all(v));
    for(ll i=0;i<n;i++)cout<<v[i]<<" ";
    cout<<nl;
}
int main(){
    FAST;
    int t=1;
    cin>>t;
    while(t--){
        solve();
    }
    return 0;
}